        <h2 class="left-mark">DESCRIPCIÓN DEL PROYECTO</h2>

        <p>El proyecto pretende mejorar la salud ocular y reducir la ceguera prevenible y/o curable de la población infantil incrementando las capacidades formativas de los profesionales en salud ocular. Impartimos formación terciaria a oftalmólogos de la Facultad de Medicina de la UCAD especializándolos en oftalmología pediátrica y capacitamos en esta misma especialidad al personal de apoyo de estos médicos, los Técnicos Superiores en Oftalmología (TSO). A la par, reforzamos una consulta de oftalmología pediátrica y un aula de formación en el principal hospital de Dakar.</p>

        <p>El equipo de OCULARIS, compuesto por dos cirujanos en Oftalmopediatría, se desplaza 3 veces al año, durante 2 semanas, a los principales Hospitales de Senegal (Abass N’dao y Le Dantec) coordinados por la UCAD, donde formamos a 3 oftalmólogos residentes y a los 2 responsables de oftalmología de dichos hospitales, tratando siempre a pacientes menores de 14 años con patologías oculares. La duración del curso es de 2 años, más 1 opcional para técnicas avanzadas.</p>

        <p>Una vez finalizada esta primera fase, se impartirá el curso a 3 nuevos oftalmólogos residentes, acompañados por los 3 médicos formados en la primera fase. La pertenencia de todos los beneficiarios del proyecto al sistema público de salud de Senegal, contribuye a garantizar la sostenibilidad del proyecto, ya que una vez concluido, les permitirá autoformar a sus futuras generaciones de oftalmólogos y TSOs en la especialidad de oftalmología pediát rica.</p>

