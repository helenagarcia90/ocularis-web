        <h2 class="left-mark">ORIGEN DEL PROYECTO</h2>

        <p>A finales de 2010 la Cátedra UNESCO de la Salud Visual perteneciente a la Universidad Politécnica de Catalunya (UPC) comienza a gestionar contactos con profesionales en el marco de los congresos de la International Agency for the Prevention of Blindness (IAPB) y la World Council of Optometry (WCO), así como con  profesionales de la Facultad de Óptica y Optometría de Terrassa (FOOT-UPC) y con OCULARIS. Estás sinergias permiten organizar en marzo de 2011, un taller en el Hospital St. Joan de Déu de Barcelona que contó con la asistencia del Dr. Senanu Quacoe (Director Regional de África Occidental de la IAPB), de representantes de la FOOT-UPC y de responsables de OCULARIS. En él, se definieron las líneas de colaboración entre las instituciones para el presente proyecto DAKAR.</p>

        <p>En el período 2012-2013 se inicia la 1a fase del proyecto que consiste en la impartición de seminarios, identificación de los actores, personal a capacitar y prioridades del país. Una delegación del Ministerio de Salud de Senegal y de la Université Cheikh Anta Diop de Dakar (UCAD) viaja a Barcelona para iniciar la concreción de las acciones. Durante el trascurso del 2013 y financiado por el programa “Terrassa Solidaria” del Ilmo. Ayuntamiento de Terrassa, se inicia el proceso de formación en el que se ejecutan dos comisiones por parte de personal del consorcio, dedicados a dar inicio a la formación de la especialidad en oftalmología pediátrica en la UCAD, firma de acuerdos y planificación de las actividades. En el consorcio también contamos con la colaboración de la Fundación Ferreruela, encargada de parte de la logística y relaciones institucionales, gracias a su dilatada experiencia en el país.</p>

        <p>En estos momentos se está trabajando para homologar la formación impartida por OCULARIS en un título reconocido por la UCAD y por su Ministerios de Sanidad y de Educación.</p>

