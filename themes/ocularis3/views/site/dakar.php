            <p>Para hacer frente a estos graves problemas, tan sólo cuentan con 58 oftalmólogos, 3 por cada millón de habitantes, de los cuales 48 se encuentran en la capital, Dakar, con lo que las zonas rurales quedan prácticamente desatendidas. La escasez de personal también se refleja en el bajo número de profesionales de atención primaria de la salud ocular, con tan solo 76 Técnicos Superiores en Oftalmología (TSO) para todo el país y consecuentemente solamente 4 de las 11 regiones del país ofrecen atención primaria de la salud visual. El reducido personal se encuentra enfrentado a situaciones difíciles de trabajo: con insuficiente equipamiento de las estructuras de la salud, baja remuneración e insuficiente formación y reciclaje.</p>

            <p>En Senegal, la salud ocular ha sido durante mucho tiempo ausente en los discursos oficiales y en las prioridades de salud identificadas por las autoridades. El I Plan Estratégico Nacional de Prevención y de Lucha contra la Ceguera se puso en marcha en enero de 2006. Respecto a la lucha contra la ceguera infantil, el Plan establece dos prioridades. La primera es la reducción de la catarata y el glaucoma congénito mediante una mejora de la formación del personal de primer nivel de detección, capacitación de oftalmólogos en cirugía pediátrica y creación un centro de referencia de cirugía pediátrica. La segunda pretende la reducción de la ceguera infantil por sarampión o falta de vitamina A mediante el apoyo de programas de salud transversal como el AIEPI (Atención Integrada a las Enfermedades de la Infancia). OCULARIS colabora con su gobierno en la capacitación de los oftalmólogos pediátricos, los cuales serán los primeros formados en esta especialidad de toda el africa francófona.</p>

            <p>Las autoridades nacionales reconocen en su plan de lucha contra la ceguera la necesidad de incrementar la coordinación de los servicios de atención ocular, disminuir la complejidad de los procedimientos administrativos y financieros para la provisión de los recursos, mejorar los servicios en los hospitales regionales y capacitar a las enfermeras para la atención de la salud ocular.</p>

